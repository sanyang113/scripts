function enter(pi) {    
    gainItem(pi);
	var prize = pi.getChannelServer().getCurrEvent().getNextPrizeCount();
	if(prize <= 3) {
		gainTopPrizes(pi);
		pi.worldMessage(0, "恭喜玩家" + pi.getPlayer().getName() + "通過了終極忍耐，名次為第"+prize+"名！");
	}
    pi.warp(109050000, 0);
}

function gainItem(pi) {
    var random = Math.floor(Math.random() * items.length);
	qty = calculateQty(items[random]);
    pi.getPlayer().gainItemName(items[random],qty);
}

function gainTopPrizes(pi) {
	var random = Math.floor(Math.random() * topItems.length);
    pi.getPlayer().gainItemName(topItems[random],1);
}

